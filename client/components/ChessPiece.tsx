export default function ChessPiece({ type, color, size, isSelected }: ChessPieceProps) {
  const animatedStyle = useAnimatedStyle(() => ({
    transform: [
      { scale: withSpring(isSelected ? 1.2 : 1, springConfig) },
      { translateY: withSpring(isSelected ? -6 : 0, springConfig) },
    ],
    shadowOpacity: withSpring(isSelected ? 0.4 : 0.2, springConfig),
  }));

  const renderPiece = () => {
    switch (type) {
      case 'king':
        return <KingPiece color={color} size={size} />;
      case 'queen':
        return <QueenPiece color={color} size={size} />;
      case 'rook':
        return <RookPiece color={color} size={size} />;
      case 'bishop':
        return <BishopPiece color={color} size={size} />;
      case 'knight':
        return <KnightPiece color={color} size={size} />;
      case 'pawn':
        return <PawnPiece color={color} size={size} />;
      default:
        return null;
    }
  };

  return (
    <AnimatedView style={[styles.container, animatedStyle, { width: size, height: size }]}>
      {renderPiece()}
    </AnimatedView>
  );
}

const styles = StyleSheet.create({
  container: {
    alignItems: 'center',
    justifyContent: 'center',
    shadowColor: '#000',
    shadowOffset: { width: 0, height: 2 },
    shadowOpacity: 0.2,
    shadowRadius: 4,
    elevation: 4,
  },
});
